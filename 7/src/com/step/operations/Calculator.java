package com.step.operations;

public class Calculator {
    public void sumInt(int firstNumber, int secondNumber){
        System.out.println("Sum is "+ (firstNumber+secondNumber));
    }
    public void sumDouble(double firstNumber, double secondNumber){
        System.out.println("Sum is "+ (firstNumber+secondNumber));
    }
    public void minusInt(int firstNumber, int secondNumber){
        System.out.println("Sum is "+ (firstNumber-secondNumber));
    }
    public void minusDouble(double firstNumber, double secondNumber){
        System.out.println("Sum is "+ (firstNumber-secondNumber));
    }
    public void multiInt(int firstNumber, int secondNumber){
        System.out.println("Sum is "+ (firstNumber*secondNumber));
    }
    public void multiDouble(double firstNumber, double secondNumber){
        System.out.println("Sum is "+ (firstNumber*secondNumber));
    }
    public void divideInt(int firstNumber, int secondNumber){
        System.out.println("Sum is "+ (firstNumber/secondNumber));
    }
    public void divideDouble(double firstNumber, double secondNumber){
        System.out.println("Sum is "+ (firstNumber/secondNumber));
    }
}
